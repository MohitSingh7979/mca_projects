public class datatypes {
    public static void main(String[] args) {
        int intNo = 51;
        byte byteNo = 1;
        short shortNo = 51;
        long longNo = 2200101;
        // float floatNo = 2.0;
        double doubleNo = 2.20010120051;
        char charVar = 'M';
        boolean tr_fls = true;
        String string = "Mohit Singh";

        System.out.println("Byte: " + byteNo);
        System.out.println("Short: " + shortNo);
        System.out.println("Number: " + intNo);
        System.out.println("Single Character: " + charVar);
        System.out.println("Long Number: " + longNo);
        // System.out.println("Floar Number" + floatNo);
        System.out.println("Double Number: " + doubleNo);
        System.out.println("Boolean: " + tr_fls);
        System.out.println("String: " + string);

    }

}
